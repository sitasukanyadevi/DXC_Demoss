namespace MyLibrary
{
    public class Calculator
    {
        public int Sum(int a, int b)
        {
            return a + b;
        }
        public int Substract(int a,int b)
        {
            return a-b;
        }
        public int Multiply(int a,int b)
        {
            return a * b;
        }
        public int Division(int a, int b)
        {
            return a / b;
        }
    }


}